/**---------------------------------------------------------------------------------------------------------------------
 * A parameterless function that returns a value.
 *--------------------------------------------------------------------------------------------------------------------*/

export type Supplier<T> = () => T;

/**---------------------------------------------------------------------------------------------------------------------
 * A single value or an array.
 *--------------------------------------------------------------------------------------------------------------------*/

export type ValueOrArray<T> = T | T[];

/**---------------------------------------------------------------------------------------------------------------------
 * A single value or a readonly array
 *--------------------------------------------------------------------------------------------------------------------*/

export type ValueOrReadonlyArray<T> = T | ReadonlyArray<T>;

/**---------------------------------------------------------------------------------------------------------------------
 * A value or a supplier thereof.
 *--------------------------------------------------------------------------------------------------------------------*/

export type ValueOrSupplier<T> = T | Supplier<T>;

/**---------------------------------------------------------------------------------------------------------------------
 * A single value, an array, or a supplier for either.
 *--------------------------------------------------------------------------------------------------------------------*/

export type ValueArrayOrSupplier<T> = T | T[] | ValueOrSupplier<T | T[]>;
